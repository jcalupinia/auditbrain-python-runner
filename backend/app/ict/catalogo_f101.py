def get_casillero_name(casillero: str, fallback: str = "") -> str:
    """Devuelve el nombre oficial del casillero F-101. Si no existe en
    el catálogo, devuelve `fallback` (no levanta excepción) — pero la
    REGLA estática del proyecto exige que todos los casilleros
    parseables estén en este catálogo, así que `fallback` solo debería
    aparecer en casos de error de configuración."""
    return F101_CASILLERO_NAMES.get(str(casillero).strip(), fallback)
